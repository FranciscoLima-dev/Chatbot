def termos(*grupos):
    vistos = set()
    resultado = []
    for grupo in grupos:
        for termo in grupo:
            termo_limpo = " ".join(str(termo).strip().split())
            chave = termo_limpo.lower()
            if termo_limpo and chave not in vistos:
                vistos.add(chave)
                resultado.append(termo_limpo)
    return ",".join(resultado)


